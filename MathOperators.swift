//  Using Swift math operators.

import Foundation

// Addition
print("The sum of 2 + 3 is \(2 + 3)")
// Subtraction
print("The difference of 2 - 3 is \(2 - 3)")
// Division
print("10 divided by 6 is " + (String(10.0 / 6.0)))
// Multiplication
print("The product of 2 and 3 is " + (String(2*3)))
// Power
print("2 to the power of 3 is " + String(pow(Float(2),Float(3))))
// Square root
print("The root of 4 is " + String(sqrt(4)))